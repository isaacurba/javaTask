public class TripleZero {
    public static void main(String[] args) {
    
    
    
        for (int hundred = 0; hundred <= 9; hundred++) {

            for (int tens = 0; tens <= 9; tens++) {

                for (int unit = 0; unit <= 9; unit++) {

                    System.out.println(unit);
        
                }
            }

        }
        
        
        
    }
}

